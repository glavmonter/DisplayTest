void InitEncoderTimer(void) {
    hTimEncoder.Instance                = TIM3;
    hTimEncoder.Init.Period             = 65535;
    hTimEncoder.Init.Prescaler          = 0;
    hTimEncoder.Init.ClockDivision      = 0;
    hTimEncoder.Init.CounterMode        = TIM_COUNTERMODE_UP;
    hTimEncoder.Init.RepetitionCounter  = 0;

    sEncoderConfig.EncoderMode          = TIM_ENCODERMODE_TI12;
    
    sEncoderConfig.IC1Polarity          = TIM_ICPOLARITY_RISING;
    sEncoderConfig.IC1Selection         = TIM_ICSELECTION_DIRECTTI;
    sEncoderConfig.IC1Prescaler         = TIM_ICPSC_DIV1;
    sEncoderConfig.IC1Filter            = 10;

    sEncoderConfig.IC2Polarity          = TIM_ICPOLARITY_RISING;
    sEncoderConfig.IC2Selection         = TIM_ICSELECTION_DIRECTTI;
    sEncoderConfig.IC2Prescaler         = TIM_ICPSC_DIV1;
    sEncoderConfig.IC2Filter            = 10;

    if (HAL_TIM_Encoder_Init(&hTimEncoder, &sEncoderConfig) != HAL_OK) {
        assert_param(0);
    }

    __HAL_TIM_SetCounter(&hTimEncoder, 0xFFFF >> 1);
    HAL_TIM_Encoder_Start(&hTimEncoder, TIM_CHANNEL_ALL);
}
